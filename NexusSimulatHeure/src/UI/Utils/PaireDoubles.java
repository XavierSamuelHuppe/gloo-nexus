package UI.Utils;

public class PaireDoubles {
    private double premier;
    private double second;
    
    public PaireDoubles(double premier, double second)
    {
        this.premier = premier;
        this.second = second;
    }
    
    public double getPremier()
    {
        return this.premier;
    }
    
    public double getSecond()            
    {
        return this.second;
    }
}
